log(chalk.greenBright('Hello world!'));
log(chalk.yellowBright('Hello world!'));
log(chalk.blueBright('Hello world!'));
log(chalk.magentaBright('Hello world!'));
log(chalk.cyanBright('Hello world!'));
log(chalk.whiteBright('Hello world!'));

