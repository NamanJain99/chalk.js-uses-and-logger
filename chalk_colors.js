const chalk = require('chalk');

const log = console.log;

// Colors
// black
// red
// green
// yellow
// blue
// magenta
// cyan
// white
// blackBright (alias: gray, grey)
// redBright
// greenBright
// yellowBright
// blueBright
// magentaBright
// cyanBright
// whiteBright
log(chalk.black('Hello world!'));
log(chalk.red('Hello world!'));
log(chalk.green('Hello world!'));
log(chalk.yellow('Hello world!'));
log(chalk.blue('Hello world!'));
log(chalk.magenta('Hello world!'));
log(chalk.cyan('Hello world!'));
log(chalk.white('Hello world!'));

log(chalk.blackBright('Hello world!'));
